package build

import (
	"bytes"
	"fmt"
	"path/filepath"
	"strings"
	"text/template"

	"strait/internal/domain"
)

// DockerfileSpec describes the inputs needed to generate a deployment Dockerfile.
type DockerfileSpec struct {
	// Runtime is the target programming language.
	Runtime domain.Runtime

	// BaseImage overrides the default base image for this runtime.
	// Leave empty to use the canonical ghcr.io/strait-dev/runtime-{lang}:latest.
	BaseImage string

	// JobSlug is used in image labels and the default CMD argument.
	JobSlug string

	// DepsFile is the path to the dependency manifest within the tarball.
	// Auto-detected from the runtime when empty (requirements.txt, package.json, etc.).
	DepsFile string
}

// GenerateDockerfile returns the Dockerfile content for a code-first deployment.
// The output is deterministic for a given DockerfileSpec so BuildKit can cache it.
func GenerateDockerfile(spec DockerfileSpec) (string, error) {
	if !spec.Runtime.IsValid() {
		return "", fmt.Errorf("build: unsupported runtime %q", spec.Runtime)
	}

	if spec.BaseImage == "" {
		spec.BaseImage = DefaultBaseImage(spec.Runtime)
	}

	if spec.DepsFile == "" {
		spec.DepsFile = DefaultDepsFile(spec.Runtime)
	}

	// Reject control characters in fields that are interpolated directly into
	// the Dockerfile template; a newline in any of these values would split the
	// instruction and allow injecting arbitrary Dockerfile directives.
	if err := validateDockerfileField("base_image", spec.BaseImage); err != nil {
		return "", err
	}
	if err := validateDockerfileField("job_slug", spec.JobSlug); err != nil {
		return "", err
	}
	if err := validateDockerfileDepsFile(spec.DepsFile); err != nil {
		return "", err
	}

	tmplSrc, ok := dockerfileTemplates[spec.Runtime]
	if !ok {
		return "", fmt.Errorf("build: no Dockerfile template for runtime %q", spec.Runtime)
	}

	tmpl, err := template.New("dockerfile").Parse(tmplSrc)
	if err != nil {
		return "", fmt.Errorf("build: parse dockerfile template: %w", err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, spec); err != nil {
		return "", fmt.Errorf("build: execute dockerfile template: %w", err)
	}
	return buf.String(), nil
}

// validateDockerfileField rejects values that contain characters which could
// inject additional Dockerfile instructions when interpolated by text/template.
// Newlines, carriage returns and null bytes are all disallowed.
func validateDockerfileField(field, value string) error {
	for i, ch := range value {
		if ch == '\n' || ch == '\r' || ch == 0 {
			return fmt.Errorf("build: %s contains disallowed control character at byte %d", field, i)
		}
	}
	return nil
}

// validateDockerfileDepsFile additionally rejects path traversal and absolute
// paths so the COPY instruction cannot reference files outside the build context.
func validateDockerfileDepsFile(value string) error {
	if err := validateDockerfileField("deps_file", value); err != nil {
		return err
	}
	cleaned := filepath.Clean(value)
	if filepath.IsAbs(cleaned) || strings.HasPrefix(cleaned, "..") {
		return fmt.Errorf("build: deps_file %q attempts path traversal or uses an absolute path", value)
	}
	return nil
}

// DefaultBaseImage returns the canonical base image URI for a runtime.
func DefaultBaseImage(r domain.Runtime) string {
	return "ghcr.io/strait-dev/runtime-" + string(r) + ":latest"
}

// DefaultDepsFile returns the canonical dependency manifest filename for a runtime.
func DefaultDepsFile(r domain.Runtime) string {
	switch r {
	case domain.RuntimePython:
		return "requirements.txt"
	case domain.RuntimeTypeScript:
		return "package.json"
	case domain.RuntimeRuby:
		return "Gemfile"
	case domain.RuntimeRust:
		return "Cargo.toml"
	case domain.RuntimeGo:
		return "go.mod"
	default:
		return ""
	}
}

// dockerfileTemplates holds the per-runtime Dockerfile templates.
// Templates receive a DockerfileSpec as their data.
var dockerfileTemplates = map[domain.Runtime]string{
	domain.RuntimePython: `# Generated by Strait — do not edit.
FROM {{.BaseImage}}

WORKDIR /app

LABEL strait.job="{{.JobSlug}}" strait.runtime="python"

# Install dependencies before copying code so Docker/BuildKit can cache this layer.
COPY {{.DepsFile}} ./
RUN --mount=type=cache,target=/root/.cache/pip \
    pip install --no-cache-dir -r {{.DepsFile}}

# Copy user code.
COPY . ./

CMD ["python", "-m", "strait.worker"]
`,

	domain.RuntimeTypeScript: `# Generated by Strait — do not edit.
FROM {{.BaseImage}}

WORKDIR /app

LABEL strait.job="{{.JobSlug}}" strait.runtime="typescript"

# Install dependencies before copying code.
COPY {{.DepsFile}} bun.lockb* ./
RUN --mount=type=cache,target=/root/.bun/install/cache \
    bun install --frozen-lockfile 2>/dev/null || bun install

# Copy user code.
COPY . ./

CMD ["bun", "run", "index.ts"]
`,

	domain.RuntimeRuby: `# Generated by Strait — do not edit.
FROM {{.BaseImage}}

WORKDIR /app

LABEL strait.job="{{.JobSlug}}" strait.runtime="ruby"

# Install dependencies before copying code.
COPY {{.DepsFile}} Gemfile.lock* ./
RUN --mount=type=cache,target=/usr/local/bundle \
    bundle install

# Copy user code.
COPY . ./

CMD ["ruby", "handler.rb"]
`,

	domain.RuntimeRust: `# Generated by Strait — do not edit.
# Rust uses a two-stage build: compile in the builder, run in a minimal image.
FROM {{.BaseImage}} AS builder

WORKDIR /build

LABEL strait.job="{{.JobSlug}}" strait.runtime="rust"

# Copy manifests first for dependency caching.
COPY {{.DepsFile}} Cargo.lock* ./
RUN mkdir src && echo 'fn main() {}' > src/main.rs
RUN --mount=type=cache,target=/usr/local/cargo/registry \
    --mount=type=cache,target=/build/target \
    cargo build --release

# Build user code.
COPY . ./
RUN touch src/main.rs
RUN --mount=type=cache,target=/usr/local/cargo/registry \
    --mount=type=cache,target=/build/target \
    cargo build --release && \
    cp target/release/handler /app/handler

# Minimal runtime image.
FROM debian:bookworm-slim AS runtime
RUN apt-get update && apt-get install -y --no-install-recommends ca-certificates libssl3 \
    && rm -rf /var/lib/apt/lists/*
RUN groupadd -r strait && useradd -r -g strait strait
WORKDIR /app
COPY --from=builder /app/handler ./
USER strait
CMD ["/app/handler"]
`,

	domain.RuntimeGo: `# Generated by Strait — do not edit.
# Go uses a two-stage build: compile in the builder, run in a distroless image.
FROM {{.BaseImage}} AS builder

WORKDIR /build

LABEL strait.job="{{.JobSlug}}" strait.runtime="go"

# Download dependencies before copying source for better layer caching.
COPY {{.DepsFile}} go.sum* ./
RUN --mount=type=cache,target=/root/.cache/go-build \
    --mount=type=cache,target=/go/pkg/mod \
    go mod download

# Build user code.
COPY . ./
RUN --mount=type=cache,target=/root/.cache/go-build \
    --mount=type=cache,target=/go/pkg/mod \
    CGO_ENABLED=0 GOOS=linux go build -trimpath -ldflags="-s -w" -o /app/handler .

# Minimal runtime image.
FROM gcr.io/distroless/static-debian12 AS runtime
COPY --from=builder /app/handler /app/handler
USER nonroot:nonroot
CMD ["/app/handler"]
`,
}
